/* Write a dart program  to print number spelling if number is between 0 to 5 else print number is greater than 5*/

void main (){
  int x = 4;
  if (x == 0){
    print("Zero");
  }else if (x == 1){
    print("One");
  }else if (x == 2){
    print("Two");
  }else if (x == 3){
    print("Three");
  }else if (x == 4){
    print("Four");
  }else if (x == 0){
    print("Five");
  }else{
    print("$x is greater than 5");
  }
}